class Vozilo {
    // Attributes
    private int speed;
    private double fuelCapacity;

    // Constructor
    public Vozilo(int speed, double fuelCapacity) {
        this.speed = speed;
        this.fuelCapacity = fuelCapacity;
    }

    // Getter methods
    public int getSpeed() {
        return speed;
    }

    public double getFuelCapacity() {
        return fuelCapacity;
    }
}